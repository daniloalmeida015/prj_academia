    public void checarLogin(){
        setUsuario(txtUsuario.getText());
        //setSenha(txtSenha.getText());
        setSenha(String.valueOf(jPasswordSenha.getPassword()));
        
        Boolean isPreenchido;
        isPreenchido = (!getUsuario().equals("") && !getSenha().equals(""));
        
        if(isPreenchido){
            listaLogin= sessao.createQuery("from Login where usuario ='"+getUsuario()+"' and senha='"+getSenha()+"'").list();
            
            if (!listaLogin.isEmpty()){
                login= listaLogin.get(0);
                this.dispose();
            } else{
                JOptionPane.showMessageDialog(this, "Usuario ou senha incorreto(s)!");
            }
        } else{
            JOptionPane.showMessageDialog(this, "Preencha todos os campos!");
        }
    }
    
    
    
    
