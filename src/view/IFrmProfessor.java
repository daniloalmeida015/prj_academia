    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

        linhaSelecionada= tblProfessor.getSelectedRow();
        
        if(linhaSelecionada >=0){
            Integer id = Integer.valueOf(tblProfessor.getValueAt(linhaSelecionada, 0).toString());
            professor= (Professor) sessao.get(Professor.class, id);

            int opcao = JOptionPane.showConfirmDialog(null,
                "Deseja realmente excluir "+professor.getPessoafisica().getPessoa().getNome().toUpperCase()+"? ", "Excluir professor!", JOptionPane.YES_NO_OPTION);

            if(opcao==0){          
                //deletar();
                
                JOptionPane.showMessageDialog(this, "Professor EXCLUIDO com sucesso!");
                listaProfessor.remove(professor);
                dtm.removeRow(linhaSelecionada);
            }
            txtPesquisa.setText(" PESQUISAR PROFESSOR");
        } else{
            JOptionPane.showMessageDialog(this, "Selecione um Professor primeiro");
        } 
    }//GEN-LAST:event_btnExcluirActionPerformed

