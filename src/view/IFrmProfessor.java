    private void popularTabela(){
       
        listaProfessor= sessao.createQuery("from Professor").list();
        
        if(listaProfessor!=null){
            
            for(int aux=0; aux <listaProfessor.size(); aux++){
                
                novaLinha(aux);
                 
            }
        }
        
    }
    
    
    private void novaLinha(int aux){
        
        Professor p = listaProfessor.get(aux);
        
        dtm.addRow(p.montarLinha());
    }
    
    private void atualizarTabela(){
       
        int aux = listaProfessor.size()-1;
        
        novaLinha(aux);
        
    }
    
    private void deletar(){
        try{
            sessao.beginTransaction();
                sessao.delete(professor);
            sessao.getTransaction().commit();

            JOptionPane.showMessageDialog(this, "Professor EXCLUIDO com sucesso!");
            listaProfessor.remove(professor);
            dtm.removeRow(linhaSelecionada);
        } catch(Exception erro){

            JOptionPane.showMessageDialog(this, "Erro ao EXCLUIR as informações!"+"\n"+erro+"\n Causa: "+erro.getCause());
            sessao.getTransaction().rollback();
        }
    }
    
    private void pesquisar(){
        txtPesquisa.setText(txtPesquisa.getText().toUpperCase());
        String pesquisa= txtPesquisa.getText();
        dtm.setRowCount(0);
        
        for(int i=0; i <listaProfessor.size(); i++){
               
            String nome = listaProfessor.get(i).getPessoafisica().getPessoa().getNome();
            
            if(nome.contains(pesquisa)){
               
                Professor p = listaProfessor.get(i);
                
                dtm.addRow(p.montarLinha());
            }
        }
    }
    
    
    
    
    
